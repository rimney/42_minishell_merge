#include "../../minishell.h"


void    ft_pwd(char **envp)
{
    int i;

    i = 0;
    while(envp[i])
    {
        if(ft_strncmp(envp[i], "PWD=", 4) == 0)
        {
            printf("%s\n", envp[i] + 4);
            return ;
        }
        i++;
    }
}